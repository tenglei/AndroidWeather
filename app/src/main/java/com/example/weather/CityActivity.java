    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        this.requestWindowFeature(Window.FEATURE_NO_TITLE);
        setContentView(R.layout.city_control);
        sharedPreferences= getSharedPreferences("cities", Context.MODE_PRIVATE);
        all_city = city_tmp.split("\n");
        Intent intent = getIntent();
        get_default_city = intent.getStringExtra("default_city");
        city_select = sharedPreferences.getString("city_select",get_default_city);
        show_city = sharedPreferences.getInt("show_city",0);

        city.add(new CityItem(get_default_city));

        init_city_list();
        Button get_city = (Button) this.findViewById(R.id.get_city);
        cityadapter = new CityAdapter(this, city,sharedPreferences);
        cityadapter.setSelectIndex(show_city);
        city_containter = (ListView) this.findViewById(R.id.city_container);
        city_containter.setAdapter(cityadapter);
        get_city.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View view) {
                EditText edit_city = (EditText) findViewById(R.id.edit_city);
                String city_get = edit_city.getText().toString();
                Log.v("editcity", city_get);
                boolean find = false;
                if (!city_get.equals("")) {
                    for (String i : all_city) {
                        if (i.equals(city_get)) {
                            find = true;
                            break;
                        }
                    }
                    if (find) {
                        city.add(new CityItem(city_get));
                        cityadapter.notifyDataSetChanged();
                        city_containter.setSelection(city.size() - 1);
                        Toast toast = Toast.makeText(CityActivity.this, city_get, Toast.LENGTH_SHORT);
                        toast.show();
//                        sharedPreferences.edit().putInt("city_number",city_number).commit();
                        store_city(city_get);
                        edit_city.setText("");
                    } else {
                        Toast toast = Toast.makeText(CityActivity.this, "找不到您输入的城市", Toast.LENGTH_SHORT);
                        toast.show();
                    }
                }
            }
        });
        city_containter.setOnItemClickListener(new AdapterView.OnItemClickListener() {
            @Override
            public void onItemClick(AdapterView<?> adapterView, View view, int i, long l) {
                cityadapter.setSelectIndex(i);
                TextView textView = (TextView)adapterView.getChildAt(i).findViewById(R.id.city_name);
                for(Integer j=0;j<adapterView.getChildCount();j++){
                    if(j!=i){
                        RadioButton radiobutton = (RadioButton) adapterView.getChildAt(j).findViewById(R.id.select);
                        radiobutton.setChecked(false);
                    }
                }
                cityadapter.notifyDataSetChanged();
                show_city = i;
                city_select = textView.getText().toString();
              //  Log.i("city_index",String.valueOf(show_city));
            }

        });
        Button back = (Button)findViewById(R.id.back_to_mainactivity);
        back.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View view) {
                Intent intent = new Intent();
                intent.putExtra("show_city", city_select);
                sharedPreferences.edit().putInt("show_city",show_city).commit();
                sharedPreferences.edit().putString("city_select",city_select).commit();
                setResult(0,intent);
                finish();
            }
        });

    }
    public void store_city(String city_name){
        sharedPreferences.edit().putString(city_name,city_name).commit();
    }
    public String get_city_name(String city_name){
        String result =sharedPreferences.getString(city_name,"no_city");
        Log.v("get",result);
        return result;
    }
    public void init_city_list(){
        Map<String,?> allContent = sharedPreferences.getAll();
        for(String i:allContent.keySet()){
            if(!i.equals("default_city")&&!i.equals("show_city")&&!i.equals("send_city")&&!i.equals("city_select")){
                String city_from_store=allContent.get(i).toString();
                city.add(new CityItem(city_from_store));
                Log.i("debug",i);
            }
        }
    }

